class Produto:

    def __init__(self, id, id_modelo, unidade, preco, estoqueMin, id_categoria, status, venda, preco_venda, frequencia, limite_velocidade, velocidade):

        self.id = id
        self.id_modelo = id_modelo
        self.unidade = unidade
        self.preco = preco
        self.estoqueMin = estoqueMin
        self.id_categoria = id_categoria
        self.status = status
        self.venda = venda
        self.preco_venda = preco_venda
        self.frequencia = frequencia
        self.limite_velocidade = limite_velocidade
        self.velocidade = velocidade